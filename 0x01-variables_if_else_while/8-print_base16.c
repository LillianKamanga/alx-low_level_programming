#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 *main - point of execution
 *@void: no parameters accepted
 *Tells you whether a number is negative, positive or zeroe
 *Return: 0  Program executed successfully.
 */
int main(void)
{
	char c, p;

	for (c = '0'; c <= '9'; c++)
	putchar(c);
	for (p = 'a'; p <= 'f'; p++)
	putchar(p);
	putchar('\n');

return (0);
}
